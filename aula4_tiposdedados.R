# Tipos Básicos de Dados

# Inteiro = integer
4 , 5 , 15z 
# Mas para o R interpretar com um Inteiro, declarar com L
4L , 5L , 15L


# Ponto Flutuante(float) = numeric
4, 5, 15 # Sem utilizar o L
4.5, 5.3 , 15.9 


# Strings = character
"Churros"
'Cachorro-Quente'


# Booleano = logical
TRUE ou FALSE
# pode ser também 
T ou F


# Números Complexos = complex
z = a + bi
# a = Parte de Real, b = Parte Imaginária, i = sqrt(-1)
sqrt(-81) # Erro, NaN -> Not a Number
sqrt(-81+0i) # complex
2 + 41 # complex
6i # complex


